(function () {
  // defensive DOM bindings so this script can be used on multiple page variants
  function getEl(id) {
    return document.getElementById(id);
  }

  function translatePage(lang) {
    const selected = translations[lang] || translations.en;
    Object.entries(selected).forEach(([key, value]) => {
      const el = getEl(key);
      if (el) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = value;
        } else {
          el.textContent = value;
        }
      }
    });

    // Replace plain text nodes that exactly match English source strings
    replaceTextNodes(lang);

    // toggle header buttons active state
    const headerButtons = document.querySelectorAll('.lang-btn');
    headerButtons.forEach((button) => {
      button.classList.toggle('active', button.dataset.lang === lang);
    });

    try {
      localStorage.setItem('appLanguage', lang);
      // keep legacy key for backward compatibility
      localStorage.setItem('horizonLanguage', lang);
    } catch (e) {
      // ignore storage errors
    }
  }

  function safeOpenModal(modal) {
    if (!modal) return;
    modal.style.display = 'grid';
  }

  function safeCloseModal(modal) {
    if (!modal) return;
    try {
      if (modal && modal.parentNode) modal.parentNode.removeChild(modal);
    } catch (e) {
      modal.style.display = 'none';
    }
  }

  document.addEventListener('DOMContentLoaded', function () {
    const modal = getEl('languageModal');
    const modalButtons = document.querySelectorAll('.language-buttons button');
    const headerButtons = document.querySelectorAll('.lang-btn');
    const form = document.querySelector('.contact-form');

    function setLanguage(lang) {
      translatePage(lang);
      safeCloseModal(modal);
    }

    modalButtons.forEach((button) => {
      button.addEventListener('click', () => {
        setLanguage(button.dataset.lang);
      });
    });

    headerButtons.forEach((button) => {
      button.addEventListener('click', () => {
        setLanguage(button.dataset.lang);
      });
    });

    // only open modal if there is a modal element present
    const savedLanguage = (function () {
      try {
        return localStorage.getItem('appLanguage') || localStorage.getItem('horizonLanguage');
      } catch (e) {
        return null;
      }
    })();

    if (savedLanguage) {
      translatePage(savedLanguage);
      safeCloseModal(modal);
    } else {
      safeOpenModal(modal);
    }

    if (form) {
      form.addEventListener('submit', function (event) {
        event.preventDefault();

        const nameEl = getEl('nameInput');
        const emailEl = getEl('emailInput');
        const phoneEl = getEl('phoneInput');
        const messageEl = getEl('messageInput');

        const name = nameEl ? nameEl.value.trim() : '';
        const email = emailEl ? emailEl.value.trim() : '';
        const phone = phoneEl ? phoneEl.value.trim() : '';
        const message = messageEl ? messageEl.value.trim() : '';

        if (!name || !email || !phone) {
          alert('Please fill in your name, email, and phone number.');
          return;
        }

        const subject = encodeURIComponent('Education Consultation Request');
        const body = encodeURIComponent(
          `Name: ${name}\nEmail: ${email}\nPhone: ${phone}\nMessage: ${message}`
        );
        const mailtoLink = `mailto:horizon@horizon-edu.net?subject=${subject}&body=${body}`;

        window.location.href = mailtoLink;
        form.reset();
      });
    }
  });
  
  // Build reverse lookup from English text -> translation key
  const reverseMap = (function buildReverse() {
    const map = new Map();
    const en = translations.en || {};
    Object.entries(en).forEach(([k, v]) => {
      if (typeof v === 'string') map.set(normalizeText(v), k);
    });
    return map;
  })();

  function normalizeText(s) {
    return (s || '').replace(/\s+/g, ' ').trim();
  }

  function replaceTextNodes(lang) {
    const selected = translations[lang] || translations.en;
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    const nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);

    nodes.forEach((textNode) => {
      const parent = textNode.parentNode;
      if (!parent) return;
      // skip script/style and editable areas
      const tag = parent.nodeName && parent.nodeName.toLowerCase();
      if (['script', 'style', 'code', 'pre'].includes(tag)) return;
      const orig = normalizeText(textNode.nodeValue);
      if (!orig) return;
      const key = reverseMap.get(orig);
      if (key && selected[key]) {
        textNode.nodeValue = textNode.nodeValue.replace(orig, selected[key]);
      }
    });

    // replace placeholder/title/alt attributes when they match English source
    const attrSelectors = 'input,textarea,img,button,select';
    document.querySelectorAll(attrSelectors).forEach((el) => {
      ['placeholder', 'title', 'alt', 'value'].forEach((attr) => {
        if (!el.hasAttribute(attr)) return;
        const v = normalizeText(el.getAttribute(attr));
        if (!v) return;
        const k = reverseMap.get(v);
        if (k && selected[k]) el.setAttribute(attr, selected[k]);
      });
    });
  }
})();
