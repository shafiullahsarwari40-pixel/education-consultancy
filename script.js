document.addEventListener('DOMContentLoaded', function () {
  const form = document.querySelector('.contact-form');
  const modal = document.getElementById('languageModal');
  const modalButtons = document.querySelectorAll('.language-buttons button');
  const headerButtons = document.querySelectorAll('.lang-btn');

  function translatePage(lang) {
    const selected = translations[lang] || translations.en;
    Object.entries(selected).forEach(([key, value]) => {
      const el = document.getElementById(key);
      if (el) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = value;
        } else {
          el.textContent = value;
        }
      }
    });

    headerButtons.forEach((button) => {
      button.classList.toggle('active', button.dataset.lang === lang);
    });

    localStorage.setItem('horizonLanguage', lang);
  }

  function openModal() {
    modal.style.display = 'grid';
  }

  function closeModal() {
    try {
      // remove modal from DOM so it cannot intercept pointer events
      if (modal && modal.parentNode) modal.parentNode.removeChild(modal);
    } catch (e) {
      if (modal) modal.style.display = 'none';
    }
  }

  // Close modal when clicking outside the panel
  modal.addEventListener('click', (e) => {
    if (e.target === modal) closeModal();
  });

  // Close modal with close button
  const closeBtn = document.getElementById('languageClose');
  if (closeBtn) closeBtn.addEventListener('click', closeModal);

  // Close modal on Escape
  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') closeModal();
  });

  function setLanguage(lang) {
    translatePage(lang);
    closeModal();
  }

  modalButtons.forEach((button) => {
    button.addEventListener('click', () => {
      setLanguage(button.dataset.lang);
    });
  });

  headerButtons.forEach((button) => {
    button.addEventListener('click', () => {
      setLanguage(button.dataset.lang);
    });
  });

  const savedLanguage = localStorage.getItem('horizonLanguage');
  if (savedLanguage) {
    translatePage(savedLanguage);
    closeModal();
  } else {
    openModal();
  }

  form.addEventListener('submit', function (event) {
    event.preventDefault();

    const name = document.getElementById('nameInput').value.trim();
    const email = document.getElementById('emailInput').value.trim();
    const phone = document.getElementById('phoneInput').value.trim();
    const message = document.getElementById('messageInput').value.trim();

    if (!name || !email || !phone) {
      alert('Please fill in your name, email, and phone number.');
      return;
    }

    const subject = encodeURIComponent('Education Consultation Request');
    const body = encodeURIComponent(
      `Name: ${name}\nEmail: ${email}\nPhone: ${phone}\nMessage: ${message}`
    );
    const mailtoLink = `mailto:horizon@horizon-edu.net?subject=${subject}&body=${body}`;

    window.location.href = mailtoLink;
    form.reset();
  });
});
